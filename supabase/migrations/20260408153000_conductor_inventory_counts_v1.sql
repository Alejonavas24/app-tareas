do $$
begin
  if not exists (
    select 1
    from pg_type t
    join pg_namespace n on n.oid = t.typnamespace
    where n.nspname = 'logistica_tareas'
      and t.typname = 'conteo_inventario_estado'
  ) then
    create type logistica_tareas.conteo_inventario_estado as enum (
      'pendiente',
      'aprobado',
      'rechazado'
    );
  end if;
end;
$$;
create table if not exists logistica_tareas.conteos_inventario_pendientes (
  id bigint generated by default as identity primary key,
  espacio_id bigint not null references logistica_tareas.espacios (id) on delete cascade,
  articulo_id bigint not null references logistica_tareas.articulos (id) on delete cascade,
  actor_nombre text not null,
  estado logistica_tareas.conteo_inventario_estado not null default 'pendiente',
  cantidad_teorica integer not null default 0 check (cantidad_teorica >= 0),
  cantidad_real integer not null check (cantidad_real >= 0),
  diferencia integer not null,
  reviewed_by_nombre text,
  reviewed_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint conteos_inventario_pendientes_actor_check check (length(trim(actor_nombre)) > 0),
  constraint conteos_inventario_pendientes_diferencia_check check (
    diferencia = (cantidad_real - cantidad_teorica)
    and diferencia <> 0
  ),
  constraint conteos_inventario_pendientes_unique unique (espacio_id, articulo_id, actor_nombre)
);
create index if not exists conteos_inventario_pendientes_actor_espacio_idx
  on logistica_tareas.conteos_inventario_pendientes (actor_nombre, espacio_id, estado);
drop trigger if exists tr_conteos_inventario_pendientes_touch_updated_at on logistica_tareas.conteos_inventario_pendientes;
