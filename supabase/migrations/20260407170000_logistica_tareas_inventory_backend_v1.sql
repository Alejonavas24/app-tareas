  v_tasks jsonb;
  v_linea jsonb;
  v_linea_id bigint;
  v_articulo_id bigint;
  v_external_line_id text;
  v_requeridos_iniciales integer;
  v_requeridos_finales integer;
  v_requerimiento record;
  v_prioridad record;
  v_local_disponible integer;
  v_reserva_local integer;
  v_faltante integer;
  v_cp_disponible integer;
  v_fuente_1_id bigint;
  v_fuente_2_id bigint;
  v_fuente_1_disponible integer;
  v_fuente_2_disponible integer;
  v_asignado_externo integer;
  v_detalle jsonb;
  v_total_disponible integer;
  v_asignar integer;
begin
  if jsonb_typeof(p_payload) <> 'object' then
    raise exception 'El payload debe ser un objeto JSON';
  end if;

  if v_external_event_id is null then
    raise exception 'external_event_id es obligatorio';
  end if;

  if v_fecha_evento is null then
    raise exception 'fecha_evento es obligatoria';
  end if;

  if v_espacio_codigo is null then
    raise exception 'espacio_codigo es obligatorio';
  end if;

  if jsonb_typeof(v_lineas) <> 'array' or jsonb_array_length(v_lineas) = 0 then
    raise exception 'lineas debe ser un arreglo no vacio';
  end if;

  select id
  into v_espacio_evento_id
  from logistica_tareas.espacios
  where codigo = v_espacio_codigo
    and rol = 'evento'::logistica_tareas.espacio_rol;

  if v_espacio_evento_id is null then
    raise exception 'No se encontro un espacio de evento valido para codigo: %', v_espacio_codigo;
  end if;

  select id
  into v_cp_id
  from logistica_tareas.espacios
  where codigo = 'centro_produccion'
    and rol = 'almacen'::logistica_tareas.espacio_rol;

  if v_cp_id is null then
    raise exception 'No existe el espacio centro_produccion configurado';
  end if;

  v_novios_id := logistica_tareas.get_or_create_novios(v_novio, v_novia, v_nombre_evento);
  v_semana_operativa_inicio := logistica_tareas.week_start(v_fecha_evento);
  v_semana_operativa_fin := logistica_tareas.week_end(v_fecha_evento);

  select id, plan_version
  into v_planner_evento_id, v_previous_version
  from logistica_tareas.planner_eventos
  where external_event_id = v_external_event_id
  for update;

  if found then
    perform logistica_tareas.cancel_active_plan(v_planner_evento_id, v_previous_version);
    v_plan_version := v_previous_version + 1;

    update logistica_tareas.planner_eventos
    set
      nombre = v_nombre_evento,
      novios_id = v_novios_id,
      espacio_evento_id = v_espacio_evento_id,
      fecha_evento = v_fecha_evento,
      semana_operativa_inicio = v_semana_operativa_inicio,
      semana_operativa_fin = v_semana_operativa_fin,
      plan_version = v_plan_version,
      estado_planificacion = 'borrador',
      ruptura_inventario = false,
      last_payload = p_payload,
      updated_at = timezone('utc', now())
    where id = v_planner_evento_id;
  else
    v_plan_version := 1;

    insert into logistica_tareas.planner_eventos (
      external_event_id,
      nombre,
      novios_id,
      espacio_evento_id,
      fecha_evento,
      semana_operativa_inicio,
      semana_operativa_fin,
      plan_version,
      estado_planificacion,
      ruptura_inventario,
      last_payload
    )
    values (
      v_external_event_id,
      v_nombre_evento,
      v_novios_id,
      v_espacio_evento_id,
      v_fecha_evento,
      v_semana_operativa_inicio,
      v_semana_operativa_fin,
      v_plan_version,
      'borrador',
      false,
      p_payload
    )
    returning id into v_planner_evento_id;
  end if;

  for v_linea in
    select value
    from jsonb_array_elements(v_lineas)
  loop
    v_external_line_id := nullif(trim(v_linea ->> 'external_line_id'), '');
    v_requeridos_iniciales := nullif(trim(v_linea ->> 'cantidad'), '')::integer;

    if v_external_line_id is null then
      raise exception 'Cada linea debe incluir external_line_id';
    end if;

    if v_requeridos_iniciales is null or v_requeridos_iniciales <= 0 then
      raise exception 'La linea % debe incluir una cantidad positiva', v_external_line_id;
    end if;

    v_articulo_id := logistica_tareas.resolve_articulo_ref(coalesce(v_linea ->> 'articulo_ref', v_linea ->> 'articulo_id'));

    select ceil(v_requeridos_iniciales * (1 + coeficiente))::integer
    into v_requeridos_finales
    from logistica_tareas.articulos
    where id = v_articulo_id;

    insert into logistica_tareas.planner_lineas (
      planner_evento_id,
      plan_version,
      external_line_id,
      articulo_id,
      requeridos_iniciales,
      requeridos_finales
    )
    values (
      v_planner_evento_id,
      v_plan_version,
      v_external_line_id,
      v_articulo_id,
      v_requeridos_iniciales,
      greatest(v_requeridos_finales, 1)
    )
    returning id into v_linea_id;

    if exists (
      select 1
      from logistica_tareas.articulo_componentes
      where articulo_paquete_id = v_articulo_id
    ) then
      insert into logistica_tareas.planner_requerimientos (
        planner_evento_id,
        plan_version,
        planner_linea_id,
        articulo_solicitado_id,
        articulo_id,
        cantidad_requerida
      )
      select
        v_planner_evento_id,
        v_plan_version,
        v_linea_id,
        v_articulo_id,
        componente.articulo_componente_id,
        greatest(v_requeridos_finales, 1) * componente.cantidad_componente
      from logistica_tareas.articulo_componentes componente
      where componente.articulo_paquete_id = v_articulo_id;
    else
      insert into logistica_tareas.planner_requerimientos (
        planner_evento_id,
        plan_version,
        planner_linea_id,
        articulo_solicitado_id,
        articulo_id,
        cantidad_requerida
      )
      values (
        v_planner_evento_id,
        v_plan_version,
        v_linea_id,
        v_articulo_id,
        v_articulo_id,
        greatest(v_requeridos_finales, 1)
      );
    end if;
  end loop;

  for v_requerimiento in
    select *
    from logistica_tareas.planner_requerimientos
    where planner_evento_id = v_planner_evento_id
      and plan_version = v_plan_version
    order by id
  loop
    perform logistica_tareas.ensure_inventory_row(v_espacio_evento_id, v_requerimiento.articulo_id);
    perform logistica_tareas.ensure_inventory_row(v_cp_id, v_requerimiento.articulo_id);

    select espacio_fuente_id
    into v_fuente_1_id
    from logistica_tareas.prioridades_abastecimiento
    where espacio_evento_id = v_espacio_evento_id
      and prioridad = 1;

    select espacio_fuente_id
    into v_fuente_2_id
    from logistica_tareas.prioridades_abastecimiento
    where espacio_evento_id = v_espacio_evento_id
      and prioridad = 2;

    if v_fuente_1_id is not null then
      perform logistica_tareas.ensure_inventory_row(v_fuente_1_id, v_requerimiento.articulo_id);
    end if;

    if v_fuente_2_id is not null then
      perform logistica_tareas.ensure_inventory_row(v_fuente_2_id, v_requerimiento.articulo_id);
    end if;

    for v_prioridad in
      select id
      from logistica_tareas.inventario
      where articulo_id = v_requerimiento.articulo_id
        and espacio_id in (
          v_espacio_evento_id,
          v_cp_id,
          coalesce(v_fuente_1_id, -1),
          coalesce(v_fuente_2_id, -1)
        )
      order by espacio_id
      for update
    loop
      null;
    end loop;

    v_detalle := '[]'::jsonb;
    v_local_disponible := logistica_tareas.available_quantity(v_espacio_evento_id, v_requerimiento.articulo_id, v_semana_operativa_inicio);
    v_reserva_local := least(v_requerimiento.cantidad_requerida, v_local_disponible);

    if v_reserva_local > 0 then
      insert into logistica_tareas.inventario_movimientos (
        planner_evento_id,
        plan_version,
        planner_requerimiento_id,
        semana_operativa_inicio,
        tipo,
        estado,
        espacio_origen_id,
        espacio_destino_id,
        articulo_id,
        cantidad
      )
      values (
        v_planner_evento_id,
        v_plan_version,
        v_requerimiento.id,
        v_semana_operativa_inicio,
        'reserva_local',
        'activo',
        v_espacio_evento_id,
        null,
        v_requerimiento.articulo_id,
        v_reserva_local
      );

      v_detalle := v_detalle || jsonb_build_array(
        jsonb_build_object(
          'tipo', 'reserva_local',
          'espacio_origen_id', v_espacio_evento_id,
          'cantidad', v_reserva_local
        )
      );
    end if;

    v_faltante := v_requerimiento.cantidad_requerida - v_reserva_local;
    v_asignado_externo := 0;

    if v_faltante > 0 then
      v_cp_disponible := logistica_tareas.available_quantity(v_cp_id, v_requerimiento.articulo_id, v_semana_operativa_inicio);
      v_fuente_1_disponible := coalesce(logistica_tareas.available_quantity(v_fuente_1_id, v_requerimiento.articulo_id, v_semana_operativa_inicio), 0);
      v_fuente_2_disponible := coalesce(logistica_tareas.available_quantity(v_fuente_2_id, v_requerimiento.articulo_id, v_semana_operativa_inicio), 0);

      if v_cp_disponible >= v_faltante then
        perform logistica_tareas.create_transfer_task(
          v_planner_evento_id,
          v_plan_version,
          v_requerimiento.id,
          v_semana_operativa_inicio,
          v_cp_id,
          v_espacio_evento_id,
          v_requerimiento.articulo_id,
          v_faltante
        );
        v_asignado_externo := v_faltante;
        v_detalle := v_detalle || jsonb_build_array(
          jsonb_build_object(
            'tipo', 'reserva_traslado',
            'espacio_origen_id', v_cp_id,
            'espacio_destino_id', v_espacio_evento_id,
            'cantidad', v_faltante
          )
        );
      elsif v_fuente_1_id is not null and v_fuente_1_disponible >= v_faltante then
        perform logistica_tareas.create_transfer_task(
          v_planner_evento_id,
          v_plan_version,
          v_requerimiento.id,
          v_semana_operativa_inicio,
          v_fuente_1_id,
          v_espacio_evento_id,
          v_requerimiento.articulo_id,
          v_faltante
        );
        v_asignado_externo := v_faltante;
        v_detalle := v_detalle || jsonb_build_array(
          jsonb_build_object(
            'tipo', 'reserva_traslado',
            'espacio_origen_id', v_fuente_1_id,
            'espacio_destino_id', v_espacio_evento_id,
            'cantidad', v_faltante
          )
        );
      elsif v_fuente_2_id is not null and v_fuente_2_disponible >= v_faltante then
        perform logistica_tareas.create_transfer_task(
          v_planner_evento_id,
          v_plan_version,
          v_requerimiento.id,
          v_semana_operativa_inicio,
          v_fuente_2_id,
          v_espacio_evento_id,
          v_requerimiento.articulo_id,
          v_faltante
        );
        v_asignado_externo := v_faltante;
        v_detalle := v_detalle || jsonb_build_array(
          jsonb_build_object(
            'tipo', 'reserva_traslado',
            'espacio_origen_id', v_fuente_2_id,
            'espacio_destino_id', v_espacio_evento_id,
            'cantidad', v_faltante
          )
        );
      else
        v_total_disponible := coalesce(v_cp_disponible, 0) + coalesce(v_fuente_1_disponible, 0) + coalesce(v_fuente_2_disponible, 0);

        if v_total_disponible >= v_faltante then
          v_asignar := least(coalesce(v_cp_disponible, 0), v_faltante);
          if v_asignar > 0 then
            perform logistica_tareas.create_transfer_task(
              v_planner_evento_id,
              v_plan_version,
              v_requerimiento.id,
              v_semana_operativa_inicio,
              v_cp_id,
              v_espacio_evento_id,
              v_requerimiento.articulo_id,
              v_asignar
            );
            v_asignado_externo := v_asignado_externo + v_asignar;
            v_detalle := v_detalle || jsonb_build_array(
              jsonb_build_object(
                'tipo', 'reserva_traslado',
                'espacio_origen_id', v_cp_id,
                'espacio_destino_id', v_espacio_evento_id,
                'cantidad', v_asignar
              )
            );
          end if;

          v_asignar := least(coalesce(v_fuente_1_disponible, 0), v_faltante - v_asignado_externo);
          if v_asignar > 0 then
            perform logistica_tareas.create_transfer_task(
              v_planner_evento_id,
              v_plan_version,
              v_requerimiento.id,
              v_semana_operativa_inicio,
              v_fuente_1_id,
              v_espacio_evento_id,
              v_requerimiento.articulo_id,
              v_asignar
            );
            v_asignado_externo := v_asignado_externo + v_asignar;
            v_detalle := v_detalle || jsonb_build_array(
              jsonb_build_object(
                'tipo', 'reserva_traslado',
                'espacio_origen_id', v_fuente_1_id,
                'espacio_destino_id', v_espacio_evento_id,
                'cantidad', v_asignar
              )
            );
          end if;

          v_asignar := least(coalesce(v_fuente_2_disponible, 0), v_faltante - v_asignado_externo);
          if v_asignar > 0 then
            perform logistica_tareas.create_transfer_task(
              v_planner_evento_id,
              v_plan_version,
              v_requerimiento.id,
              v_semana_operativa_inicio,
              v_fuente_2_id,
              v_espacio_evento_id,
              v_requerimiento.articulo_id,
              v_asignar
            );
            v_asignado_externo := v_asignado_externo + v_asignar;
            v_detalle := v_detalle || jsonb_build_array(
              jsonb_build_object(
                'tipo', 'reserva_traslado',
                'espacio_origen_id', v_fuente_2_id,
                'espacio_destino_id', v_espacio_evento_id,
                'cantidad', v_asignar
              )
            );
          end if;
        end if;
      end if;
    end if;

    update logistica_tareas.planner_requerimientos
    set
      cantidad_reservada_local = v_reserva_local,
      cantidad_asignada_externa = v_asignado_externo,
      cantidad_en_ruptura = greatest(v_requerimiento.cantidad_requerida - v_reserva_local - v_asignado_externo, 0),
      estado_cobertura = case
        when (v_requerimiento.cantidad_requerida - v_reserva_local - v_asignado_externo) > 0
          then 'ruptura'::logistica_tareas.requerimiento_estado
        else 'cubierto'::logistica_tareas.requerimiento_estado
      end,
      detalle_cobertura = v_detalle
    where id = v_requerimiento.id;

    if (v_requerimiento.cantidad_requerida - v_reserva_local - v_asignado_externo) > 0 then
      v_tiene_ruptura := true;
    end if;
  end loop;

  update logistica_tareas.planner_eventos
  set
    estado_planificacion = case
      when v_tiene_ruptura then 'ruptura'::logistica_tareas.planner_estado
      else 'planificado'::logistica_tareas.planner_estado
    end,
    ruptura_inventario = v_tiene_ruptura,
    updated_at = timezone('utc', now())
  where id = v_planner_evento_id;

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'planner_requerimiento_id', pr.id,
        'planner_linea_id', pr.planner_linea_id,
        'articulo_id', pr.articulo_id,
        'articulo_nombre', a.nombre,
        'articulo_solicitado_id', pr.articulo_solicitado_id,
        'cantidad_requerida', pr.cantidad_requerida,
        'cantidad_reservada_local', pr.cantidad_reservada_local,
        'cantidad_asignada_externa', pr.cantidad_asignada_externa,
        'cantidad_en_ruptura', pr.cantidad_en_ruptura,
        'estado_cobertura', pr.estado_cobertura,
        'detalle_cobertura', pr.detalle_cobertura
      )
      order by pr.id
    ),
    '[]'::jsonb
  )
  into v_requirements
  from logistica_tareas.planner_requerimientos pr
  join logistica_tareas.articulos a on a.id = pr.articulo_id
  where pr.planner_evento_id = v_planner_evento_id
    and pr.plan_version = v_plan_version;

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'tarea_id', t.id,
        'articulo_id', t.articulo_id,
        'articulo_nombre', a.nombre,
        'cantidad', t.cantidad,
        'estado', t.estado,
        'remitente_espacio_id', t.remitente_espacio_id,
        'remitente_codigo', origen.codigo,
        'receptor_espacio_id', t.receptor_espacio_id,
        'receptor_codigo', destino.codigo
      )
      order by t.id
    ),
    '[]'::jsonb
  )
  into v_tasks
  from logistica_tareas.tareas t
  join logistica_tareas.articulos a on a.id = t.articulo_id
  join logistica_tareas.espacios origen on origen.id = t.remitente_espacio_id
  join logistica_tareas.espacios destino on destino.id = t.receptor_espacio_id
  where t.planner_evento_id = v_planner_evento_id
    and t.plan_version = v_plan_version;

  return jsonb_build_object(
    'planner_evento_id', v_planner_evento_id,
    'plan_version', v_plan_version,
    'semana_operativa_inicio', v_semana_operativa_inicio,
    'semana_operativa_fin', v_semana_operativa_fin,
    'estado_planificacion', case when v_tiene_ruptura then 'ruptura' else 'planificado' end,
    'ruptura_inventario', v_tiene_ruptura,
    'tareas_creadas', jsonb_array_length(v_tasks),
    'tareas', v_tasks,
    'requerimientos', v_requirements
  );
end;
$function$;
create or replace function logistica_tareas.complete_task(
  p_tarea_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path to 'logistica_tareas', 'public'
as $function$
declare
  v_tarea logistica_tareas.tareas%rowtype;
  v_movimiento logistica_tareas.inventario_movimientos%rowtype;
  v_stock_origen integer;
begin
  select *
  into v_tarea
  from logistica_tareas.tareas
  where id = p_tarea_id
  for update;

  if not found then
    raise exception 'No existe la tarea %', p_tarea_id;
  end if;

  if v_tarea.estado = 'completada'::logistica_tareas.tarea_estado then
    raise exception 'La tarea % ya esta completada', p_tarea_id;
  end if;

  if v_tarea.estado = 'cancelada'::logistica_tareas.tarea_estado then
    raise exception 'La tarea % esta cancelada', p_tarea_id;
  end if;

  select *
  into v_movimiento
  from logistica_tareas.inventario_movimientos
  where tarea_id = p_tarea_id
    and estado = 'activo'::logistica_tareas.movimiento_estado
  order by id desc
  limit 1
  for update;

  if not found then
    raise exception 'La tarea % no tiene una reserva de traslado activa', p_tarea_id;
  end if;

  perform logistica_tareas.ensure_inventory_row(v_tarea.remitente_espacio_id, v_tarea.articulo_id);
  perform logistica_tareas.ensure_inventory_row(v_tarea.receptor_espacio_id, v_tarea.articulo_id);

  select cantidad
  into v_stock_origen
  from logistica_tareas.inventario
  where espacio_id = v_tarea.remitente_espacio_id
    and articulo_id = v_tarea.articulo_id
  for update;

  perform 1
  from logistica_tareas.inventario
  where espacio_id = v_tarea.receptor_espacio_id
    and articulo_id = v_tarea.articulo_id
  for update;

  if v_stock_origen < v_tarea.cantidad then
    raise exception 'Inventario insuficiente para completar la tarea %', p_tarea_id;
  end if;

  update logistica_tareas.inventario
  set cantidad = cantidad - v_tarea.cantidad
  where espacio_id = v_tarea.remitente_espacio_id
    and articulo_id = v_tarea.articulo_id;

  update logistica_tareas.inventario
  set cantidad = cantidad + v_tarea.cantidad
  where espacio_id = v_tarea.receptor_espacio_id
    and articulo_id = v_tarea.articulo_id;

  update logistica_tareas.tareas
  set
    estado = 'completada',
    completed_at = timezone('utc', now()),
    updated_at = timezone('utc', now())
  where id = p_tarea_id;

  update logistica_tareas.inventario_movimientos
  set
    estado = 'aplicado',
    applied_at = timezone('utc', now()),
    updated_at = timezone('utc', now())
  where id = v_movimiento.id;

  insert into logistica_tareas.inventario_movimientos (
    planner_evento_id,
    plan_version,
    planner_requerimiento_id,
    tarea_id,
    semana_operativa_inicio,
    tipo,
    estado,
    espacio_origen_id,
    espacio_destino_id,
    articulo_id,
    cantidad,
    applied_at
  )
  values (
    v_tarea.planner_evento_id,
    v_tarea.plan_version,
    v_tarea.planner_requerimiento_id,
    v_tarea.id,
    v_movimiento.semana_operativa_inicio,
    'traslado_completado',
    'aplicado',
    v_tarea.remitente_espacio_id,
    v_tarea.receptor_espacio_id,
    v_tarea.articulo_id,
    v_tarea.cantidad,
    timezone('utc', now())
