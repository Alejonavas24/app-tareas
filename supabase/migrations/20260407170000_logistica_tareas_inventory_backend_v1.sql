create or replace function logistica_tareas.resolve_articulo_ref(
  p_articulo_ref text
)
returns bigint
language plpgsql
stable
security definer
set search_path to 'logistica_tareas', 'public'
as $function$
declare
  v_articulo_id bigint;
  v_ref text := nullif(trim(p_articulo_ref), '');
begin
  if v_ref is null then
    raise exception 'articulo_ref es obligatorio';
  end if;

  if v_ref ~ '^\d+$' then
    select id
    into v_articulo_id
    from logistica_tareas.articulos
    where id = v_ref::bigint;
  end if;

  if v_articulo_id is null then
    select id
    into v_articulo_id
    from logistica_tareas.articulos
    where airtable_record_id = v_ref;
  end if;

  if v_articulo_id is null then
    select id
    into v_articulo_id
    from logistica_tareas.articulos
    where lower(nombre) = lower(v_ref);
  end if;

  if v_articulo_id is null then
    raise exception 'No se encontro articulo para referencia: %', v_ref;
  end if;

  return v_articulo_id;
end;
$function$;
